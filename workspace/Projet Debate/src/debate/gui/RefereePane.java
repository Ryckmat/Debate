     int newReferee(ArrayList<String> socle) {
         boolean continu=true;
         ArrayList<String> topics=new ArrayList<>();
         int count=0;
         boolean sound=false;
         
         if (socle.size() > 2){/*Alors chargement d'une partie*/
            for (int i=0; continu; i++)
            {
                if (Character.isDigit(socle.get(i+2).charAt(0)))
                {
                    count--;
                    continu=false;
                }
                else
                    topics.add(socle.get(i+2));
                count++;
            }
         }
       
         if (socle.get(0).contentEquals("Sound enabled"))
             sound=true;
            referee=new Referee(topics, sound, Integer.parseInt(socle.get(1)));
         return (count);
     }              
    

