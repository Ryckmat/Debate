    public void refreshModel() {
        ((DefaultListModel) formulasJList.getModel()).clear();
        for (String s : referee.getFormulaList()) {
            ((DefaultListModel) formulasJList.getModel()).addElement(s);
        }
        refreshArgumentTree();
    }

    private void refreshArgumentTree() {
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Argument List");
        for (Argument arg : referee.getArgumentList()) {
            DefaultMutableTreeNode argumentNode = new DefaultMutableTreeNode(arg);
            for (Formula f : arg.getSupport()) {
                argumentNode.add(new DefaultMutableTreeNode(f));
            }
            root.add(argumentNode);
        }
        ((DefaultTreeModel) jTreeArguments.getModel()).setRoot(root);
    }

